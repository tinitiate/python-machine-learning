# Machine Learning
# The functionality that gives the computers the ability to learn without
# being explicit programming, such as applying varying swith or if..else
# >><<


# >>CODE-TITLE - Types of Machine Learning
# >>CODE-NOTES<<

# Supervised learning
# The program is supplied with training input data and their desired output
# data or labels, The training data is used for learning, to comeup with a
# general rule that maps inputs to outputs.

# Unsupervised learning
# No labels are given to the learning algorithm, It finds the structure of the
# input. Unsupervised learning can be a goal in itself to possibly discovering
# hidden patterns in data or a means towards an end (feature learning).

# Reinforcement learning
# A computer program interacts with a dynamic environment in which it must
# perform a certain goal (such as driving a vehicle), without a teacher
# explicitly telling it whether it has come close to its goal. Another example
# is learning to play a game by playing against an opponent.
#
